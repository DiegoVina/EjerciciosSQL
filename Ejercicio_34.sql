UPDATE
	DIRECTORS D
SET
	D.DIRECTOR_DEAD_DATE = DATEADD('YEAR',
	1,
	(
	SELECT
		MAX(M.MOVIE_LAUNCH_DATE)
	FROM
		MOVIES M
	WHERE
		M.DIRECTOR_ID = D.DIRECTOR_ID
))
WHERE
	D.DIRECTOR_DEAD_DATE < (
	SELECT
		MAX(M.MOVIE_LAUNCH_DATE)
	FROM
		MOVIES M
	WHERE
		M.DIRECTOR_ID = D.DIRECTOR_ID
);
